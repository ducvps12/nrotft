    public void showListBoss(Player player) {
        // Kiểm tra quyền truy cập
        // if (player == null || !player.isAdmin()) {
        // return;
        // }

        player.iDMark.setMenuType(3);
        Message msg = null;

        try {
            msg = new Message(-96);
            msg.writer().writeByte(0);
            msg.writer().writeUTF("Danh sách Boss đang hoạt động");

            // Lọc danh sách boss hợp lệ
            List<Boss> activeBosses = bosses.stream()
                    .filter(boss -> {
                        int mapId = boss.data[0].getMapJoin()[0];
                        return !MapService.gI().isMapBossFinal(mapId)
                                && !MapService.gI().isMapHuyDiet(mapId)
                                && !MapService.gI().isMapYardart(mapId)
                                && !MapService.gI().isMapMaBu(mapId)
                                && !MapService.gI().isMapBlackBallWar(mapId);
                    })
                    .toList();

            msg.writer().writeByte(activeBosses.size());

            for (int i = 0; i < bosses.size(); i++) {
                Boss boss = bosses.get(i);
                int mapId = boss.data[0].getMapJoin()[0];

                // Bỏ qua boss ở các map đặc biệt
                if (MapService.gI().isMapBossFinal(mapId)
                        || MapService.gI().isMapYardart(mapId)
                        || MapService.gI().isMapHuyDiet(mapId)
                        || MapService.gI().isMapMaBu(mapId)
                        || MapService.gI().isMapBlackBallWar(mapId)) {
                    continue;
                }

                msg.writer().writeInt(i); // ID Boss
                msg.writer().writeInt(i);
                msg.writer().writeShort(boss.data[0].getOutfit()[0]); // Head

                if (player.getSession().version >= 214) {
                    msg.writer().writeShort(-1);
                }

                msg.writer().writeShort(boss.data[0].getOutfit()[1]); // Body
                msg.writer().writeShort(boss.data[0].getOutfit()[2]); // Leg

                // Tên boss
                msg.writer().writeUTF(boss.data[0].getName());

                // Thông tin trạng thái
                if (boss.zone != null && boss.zone.map != null) {
                    msg.writer().writeUTF("Trạng thái: Đang hoạt động");
                    msg.writer().writeUTF(String.format(
                            "Bản đồ: %s (ID: %d)\nKhu vực: %d",
                            boss.zone.map.mapName,
                            boss.zone.map.mapId,
                            boss.zone.zoneId));
                } else {
                    msg.writer().writeUTF("Trạng thái: Chưa xuất hiện");
                    msg.writer().writeUTF("Bản đồ: Chưa xác định");
                }
            }

            player.sendMessage(msg);

        } catch (Exception e) {
            e.printStackTrace();
            Service.gI().sendThongBao(player, "Lỗi khi tải danh sách Boss.");
        } finally {
            if (msg != null) {
                msg.cleanup();
            }
        }
    }

